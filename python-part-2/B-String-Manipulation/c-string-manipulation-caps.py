# Fitur capitalize
print(">>> Fitur .capitalize()")
judulBuku = 'belajar bahasa python'
judulBuku = judulBuku.capitalize()
print("judulBuku: ", judulBuku, "\n---------------\n")

# Fitur lower
print(">>> Fitur .lower()")
judulBuku = 'BelaJAR BAHasa PYTHON'
judulBuku = judulBuku.lower()
print("judulBuku: ", judulBuku, "\n---------------\n")

# Fitur upper
print(">>> Fitur .upper()")
judulBuku = 'Belajar bahasa pYThoN'
judulBuku = judulBuku.upper()
print("judulBuku: ", judulBuku, "\n---------------\n")